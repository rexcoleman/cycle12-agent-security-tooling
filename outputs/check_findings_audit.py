def check_hypothesis_refs(
    hypothesis_refs: list[tuple[int, int]],
    project_dir: Path,
    registry_path: str,
) -> list[Finding]:
    """Verify that every H-X reference exists in HYPOTHESIS_REGISTRY.md."""
    findings: list[Finding] = []

    reg_path = project_dir / registry_path
    if not reg_path.exists():
        # Also check common locations
        for alt in [project_dir / "HYPOTHESIS_REGISTRY.md", project_dir / "docs" / registry_path]:
            if alt.exists():
                reg_path = alt
                break

    if not reg_path.exists():
        if hypothesis_refs:
            findings.append(Finding(
                "hypothesis_refs", "CRITICAL",
                f"HYPOTHESIS_REGISTRY.md not found at {registry_path} but "
                f"{len(hypothesis_refs)} hypothesis references found in FINDINGS.md",
            ))
        else:
            findings.append(Finding(
                "hypothesis_refs", "PASS",
                "No hypothesis references and no registry file — OK",
            ))
        return findings

    # Parse registry for defined hypothesis IDs
    reg_text = reg_path.read_text(encoding="utf-8", errors="replace")
    defined_ids = set()
    for m in re.finditer(r"\bH-(\d+)\b", reg_text):
        defined_ids.add(int(m.group(1)))

    # Check each reference
    missing = []
    for h_id, line_num in hypothesis_refs:
        if h_id not in defined_ids:
            missing.append((h_id, line_num))
            findings.append(Finding(
                "hypothesis_refs", "CRITICAL",
                f"Hypothesis H-{h_id} referenced but not defined in registry",
                line=line_num,
            ))

    if not missing and hypothesis_refs:
        findings.append(Finding(
            "hypothesis_refs", "PASS",
            f"All {len(hypothesis_refs)} hypothesis references resolve to registry entries",
        ))
    elif not hypothesis_refs:
        findings.append(Finding(
            "hypothesis_refs", "WARNING",
            "No hypothesis references found in FINDINGS.md",
        ))

    return findings


