# Configuration (derived from project.yaml at generation time)
# ---------------------------------------------------------------------------

DEFAULT_FINDINGS_PATH = "FINDINGS.md"
DEFAULT_OUTPUTS_DIR = "outputs"
DEFAULT_HYPOTHESIS_REGISTRY = "HYPOTHESIS_REGISTRY.md"
FIGURE_DIRS = ["figures", "blog/images", "outputs/figures"]
PROHIBITED_LANGUAGE = ["novel", "first", "validates", "proves", "breakthrough"]
VALID_CLAIM_TAGS = ["DEMONSTRATED", "SUGGESTED", "PROJECTED", "HYPOTHESIZED"]
MIN_SEEDS = 3


# ---------------------------------------------------------------------------
# Parsing helpers
# ---------------------------------------------------------------------------

# Matches claim tags like [DEMONSTRATED], [SUGGESTED, SYNTHETIC], etc.
_CLAIM_TAG_RE = re.compile(
    r"\[("
    + "|".join(VALID_CLAIM_TAGS)
    + r")(?:,\s*[A-Z_-]+)*\]"
)

# Matches hypothesis references like H-1, H-2, H-12
_HYPOTHESIS_REF_RE = re.compile(r"\bH-(\d+)\b")

# Matches evidence artifact paths (files referenced in backticks or after "Evidence:" etc.)
_EVIDENCE_PATH_RE = re.compile(
    r"`([^`]+\.(?:json|csv|txt|png|pdf|svg|jpg|jpeg|npz|pkl))`"
)

# Matches figure references like Figure 1, Fig. 2, F1, etc.
_FIGURE_REF_RE = re.compile(
    r"(?:Figure|Fig\.?)\s*(\d+)"
    r"|\bF(\d+)\b"
)

# Matches image paths in markdown: ![...](path)
_IMAGE_PATH_RE = re.compile(
    r"!\[[^\]]*\]\(([^)]+)\)"
)

# Matches numeric values (with optional sign, commas, decimals, percent)
_NUMBER_RE = re.compile(
    r"(?<![\w\\])"
    r"([+-])?"
    r"(\d{1,3}"
    r"(?:,\d{3})*"
    r"(?:\.\d+)?)"
    r"(%?)"
    r"(?![\w])"
)


