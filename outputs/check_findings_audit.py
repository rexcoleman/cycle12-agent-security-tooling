# Check 7: Required research quality sections (Novelty, Impact, Generalization)
# ---------------------------------------------------------------------------

def check_required_sections(text: str) -> list[Finding]:
    """Verify that research quality dimension sections exist in FINDINGS.md."""
    findings: list[Finding] = []

    required_sections = {
        "Novelty Assessment": "Describes how findings advance beyond prior work",
        "Practitioner Impact": "Explains practical applications and artifacts released",
        "Generalization Analysis": "Discusses scope, transferability, and boundary conditions",
    }
    optional_sections = {
        "Cross-Domain Connections": "Describes connections to other domains (optional for single-domain)",
    }

    for section_name, description in required_sections.items():
        pattern = re.compile(r"^##\s+" + re.escape(section_name), re.MULTILINE | re.IGNORECASE)
        if pattern.search(text):
            findings.append(Finding(
                "required_sections", "PASS",
                f"Required section present: {section_name}",
            ))
        else:
            findings.append(Finding(
                "required_sections", "WARNING",
                f"Missing section: {section_name} — {description}",
            ))

    for section_name, description in optional_sections.items():
        pattern = re.compile(r"^##\s+" + re.escape(section_name), re.MULTILINE | re.IGNORECASE)
        if pattern.search(text):
            findings.append(Finding(
                "required_sections", "PASS",
                f"Optional section present: {section_name}",
            ))
        # No warning for optional sections

    return findings


