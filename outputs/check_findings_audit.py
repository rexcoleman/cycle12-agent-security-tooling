def load_all_data(outputs_dir: Path) -> dict[str, Any]:
    """Load all numeric data from JSON and CSV files in outputs/."""
    data: dict[str, Any] = {}

    if not outputs_dir.exists():
        return data

    for fpath in sorted(outputs_dir.rglob("*")):
        if not fpath.is_file():
            continue

        rel = str(fpath.relative_to(outputs_dir))
        suffix = fpath.suffix.lower()

        if suffix == ".json":
            try:
                raw = json.loads(fpath.read_text(encoding="utf-8"))
                _flatten_json(raw, rel.rsplit(".", 1)[0], data)
            except Exception:
                pass
        elif suffix == ".csv":
            try:
                with open(fpath, newline="", encoding="utf-8") as f:
                    reader = csv.DictReader(f)
                    for row_idx, row in enumerate(reader):
                        for col, val in row.items():
                            if col is None:
                                continue
                            try:
                                num = float(val.replace(",", ""))
                                key = f"{rel.rsplit('.', 1)[0]}.{col}"
                                data.setdefault(key, []).append(num)
                            except (ValueError, AttributeError):
                                pass
            except Exception:
                pass

    return data


def _flatten_json(obj: Any, prefix: str, out: dict[str, Any]) -> None:
    """Recursively flatten JSON into dotted-key -> value pairs."""
    if isinstance(obj, dict):
        for k, v in obj.items():
            key = f"{prefix}.{k}" if prefix else k
            _flatten_json(v, key, out)
    elif isinstance(obj, list):
        for i, v in enumerate(obj):
            _flatten_json(v, f"{prefix}[{i}]", out)
    elif isinstance(obj, (int, float)) and not isinstance(obj, bool):
        out[prefix] = float(obj)


def check_numeric_values(
    numbers: list[dict],
    outputs_dir: Path,
    tolerance: float = 0.01,
) -> list[Finding]:
    """Cross-reference numeric values in FINDINGS.md against output data."""
    findings: list[Finding] = []

    all_data = load_all_data(outputs_dir)
    if not all_data:
        findings.append(Finding(
            "numeric_crossref", "WARNING",
            f"No numeric data found in {outputs_dir} — cannot cross-reference",
        ))
        return findings

    # Collect all unique numeric values from data
    data_values: set[float] = set()
    for v in all_data.values():
        if isinstance(v, list):
            data_values.update(v)
        elif isinstance(v, (int, float)):
            data_values.add(float(v))

    matched = 0
    unmatched = 0

    for entry in numbers:
        val = entry["value"]
        # Check percentage: if is_pct, also check val/100
        candidates = [val]
        if entry["is_pct"]:
            candidates.append(val / 100.0)

        found = False
        for candidate in candidates:
            for dv in data_values:
                if dv == 0 and candidate == 0:
                    found = True
                    break
                if dv == 0:
                    continue
                rel_diff = abs(candidate - dv) / max(abs(dv), 1e-12)
                abs_diff = abs(candidate - dv)
                if abs_diff <= tolerance or rel_diff <= tolerance:
                    found = True
                    break
            if found:
                break

        if found:
            matched += 1
        else:
            unmatched += 1
            # Only flag if the number looks like a metric (has decimal or %)
            if "." in entry["raw"] or entry["is_pct"]:
                findings.append(Finding(
                    "numeric_crossref", "WARNING",
                    f"Numeric value {entry['raw']} not found in output data",
                    line=entry["line_num"],
                ))

    total = matched + unmatched
    if total > 0:
        pct = matched / total * 100
        sev = "PASS" if pct >= 80 else "WARNING"
        findings.append(Finding(
            "numeric_crossref", sev,
            f"{matched}/{total} numeric values ({pct:.0f}%) matched in output data",
        ))

    return findings


