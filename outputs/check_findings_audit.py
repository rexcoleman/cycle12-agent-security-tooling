def run_all_checks(
    findings_path: Path,
    project_dir: Path,
    outputs_dir: Path,
    registry_path: str,
) -> list[Finding]:
    """Run all findings audit checks."""
    all_findings: list[Finding] = []

    # Parse FINDINGS.md
    if not findings_path.exists():
        all_findings.append(Finding(
            "parse", "CRITICAL",
            f"FINDINGS.md not found at {findings_path}",
        ))
        return all_findings

    parsed = parse_findings(findings_path)
    print(f"  Parsed FINDINGS.md:")
    print(f"    Claims:      {len(parsed['claims'])}")
    print(f"    Evidence:    {len(parsed['evidence_paths'])}")
    print(f"    Hypotheses:  {len(parsed['hypothesis_refs'])}")
    print(f"    Figures:     {len(parsed['figure_refs'])}")
    print(f"    Images:      {len(parsed['image_paths'])}")
    print(f"    Numbers:     {len(parsed['numbers'])}")
    print()

    # Check 1: Evidence artifact existence
    print("  Check 1: Evidence artifact existence...")
    all_findings.extend(check_evidence_artifacts(
        parsed["claims"], parsed["evidence_paths"],
        project_dir, outputs_dir,
    ))

    # Check 2: Numeric value cross-reference
    print("  Check 2: Numeric value cross-reference...")
    all_findings.extend(check_numeric_values(
        parsed["numbers"], outputs_dir,
    ))

    # Check 3: Hypothesis references
    print("  Check 3: Hypothesis references...")
    all_findings.extend(check_hypothesis_refs(
        parsed["hypothesis_refs"], project_dir, registry_path,
    ))

    # Check 4: Figure existence
    print("  Check 4: Figure existence...")
    all_findings.extend(check_figure_existence(
        parsed["figure_refs"], parsed["image_paths"], project_dir,
    ))

    # Check 5: Prohibited language
    print("  Check 5: Prohibited language scan...")
    all_findings.extend(check_prohibited_language(
        parsed["claims"], parsed["lines"],
    ))

    # Check 6: Claim tag coverage
    print("  Check 6: Claim tag coverage...")
    all_findings.extend(check_claim_coverage(
        parsed["claims"], parsed["numbers"], parsed["text"],
    ))

    # Check 7: Required research quality sections
    print("  Check 7: Required research quality sections...")
    all_findings.extend(check_required_sections(
        parsed["text"],
    ))

    return all_findings


