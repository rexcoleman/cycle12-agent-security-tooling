def parse_findings(findings_path: Path) -> dict:
    """Parse FINDINGS.md and extract structured data.

    Returns a dict with:
      text: full text
      lines: list of (line_num, line_text) tuples
      claims: list of dicts with tag, line_num, line_text, qualifiers
      evidence_paths: list of (path_str, line_num) tuples
      hypothesis_refs: list of (h_id, line_num) tuples
      figure_refs: list of (fig_num, line_num) tuples
      image_paths: list of (path_str, line_num) tuples
      numbers: list of dicts with value, raw, is_pct, line_num, context
    """
    text = findings_path.read_text(encoding="utf-8", errors="replace")
    lines = text.splitlines()

    claims = []
    evidence_paths = []
    hypothesis_refs = []
    figure_refs = []
    image_paths = []
    numbers = []

    for i, line in enumerate(lines, start=1):
        # Extract claim tags
        for m in _CLAIM_TAG_RE.finditer(line):
            tag_text = m.group(0)
            base_tag = m.group(1)
            # Extract qualifiers
            qualifiers = [q.strip() for q in tag_text[1:-1].split(",")]
            claims.append({
                "tag": base_tag,
                "qualifiers": qualifiers,
                "line_num": i,
                "line_text": line.strip(),
                "full_tag": tag_text,
            })

        # Extract evidence paths
        for m in _EVIDENCE_PATH_RE.finditer(line):
            evidence_paths.append((m.group(1), i))

        # Extract hypothesis references
        for m in _HYPOTHESIS_REF_RE.finditer(line):
            hypothesis_refs.append((int(m.group(1)), i))

        # Extract figure references
        for m in _FIGURE_REF_RE.finditer(line):
            fig_num = m.group(1) or m.group(2)
            if fig_num:
                figure_refs.append((int(fig_num), i))

        # Extract image paths
        for m in _IMAGE_PATH_RE.finditer(line):
            image_paths.append((m.group(1), i))

        # Extract numeric values (skip headers, metadata, legends)
        stripped = line.strip()
        if stripped.startswith("#") or stripped.startswith("|") or stripped.startswith(">"):
            continue
        if stripped.startswith("**Data type") or stripped.startswith("**Seeds"):
            continue

        for m in _NUMBER_RE.finditer(line):
            raw = m.group(0)
            val_str = m.group(2).replace(",", "")
            try:
                val = float(val_str)
            except ValueError:
                continue
            if m.group(1) == "-":
                val = -val
            # Skip trivially small integers and years
            if val in (0, 1, 2, 3) and not m.group(3):
                continue
            if 1900 <= val <= 2100 and not m.group(3):
                continue

            # Build context
            idx = m.start()
            ctx_start = max(0, idx - 60)
            ctx_end = min(len(line), idx + len(raw) + 60)
            context = line[ctx_start:ctx_end].strip()

            numbers.append({
                "value": val,
                "raw": raw,
                "is_pct": m.group(3) == "%",
                "line_num": i,
                "context": context,
            })

    return {
        "text": text,
        "lines": [(i, l) for i, l in enumerate(lines, start=1)],
        "claims": claims,
        "evidence_paths": evidence_paths,
        "hypothesis_refs": hypothesis_refs,
        "figure_refs": figure_refs,
        "image_paths": image_paths,
        "numbers": numbers,
    }


