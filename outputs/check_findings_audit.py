# Check 5: Prohibited language in [DEMONSTRATED] claims
# ---------------------------------------------------------------------------

def check_prohibited_language(
    claims: list[dict],
    lines: list[tuple[int, str]],
) -> list[Finding]:
    """Scan for prohibited language near [DEMONSTRATED] claims."""
    findings: list[Finding] = []

    # Build a set of line numbers with DEMONSTRATED claims
    demonstrated_lines = {c["line_num"] for c in claims if c["tag"] == "DEMONSTRATED"}

    violations = []
    for line_num, line_text in lines:
        # Check lines with DEMONSTRATED claims (and 2 lines before/after for context)
        near_demonstrated = any(
            abs(line_num - dl) <= 2 for dl in demonstrated_lines
        )
        if not near_demonstrated:
            continue

        line_lower = line_text.lower()
        for word in PROHIBITED_LANGUAGE:
            # Word boundary match
            pattern = r"\b" + re.escape(word) + r"\b"
            if re.search(pattern, line_lower):
                # Exclude if the word is in a quote or citation
                if line_text.strip().startswith(">") or line_text.strip().startswith('"'):
                    continue
                violations.append((word, line_num, line_text.strip()[:100]))
                findings.append(Finding(
                    "prohibited_language", "CRITICAL",
                    f"Prohibited word '{word}' near [DEMONSTRATED] claim",
                    line=line_num,
                ))

    if not violations:
        findings.append(Finding(
            "prohibited_language", "PASS",
            f"No prohibited language found near {len(demonstrated_lines)} [DEMONSTRATED] claims",
        ))

    return findings


