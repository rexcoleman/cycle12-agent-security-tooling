def check_figure_existence(
    figure_refs: list[tuple[int, int]],
    image_paths: list[tuple[str, int]],
    project_dir: Path,
) -> list[Finding]:
    """Verify that every referenced figure exists in figures/ or blog/images/."""
    findings: list[Finding] = []

    # Check explicit image paths
    missing_images = []
    for img_path, line_num in image_paths:
        candidates = [project_dir / img_path]
        for fig_dir in FIGURE_DIRS:
            candidates.append(project_dir / fig_dir / Path(img_path).name)
        found = any(c.exists() for c in candidates)
        if not found:
            missing_images.append((img_path, line_num))
            findings.append(Finding(
                "figure_existence", "CRITICAL",
                f"Referenced image not found: {img_path}",
                line=line_num,
            ))

    # Check numbered figure references against files in figure directories
    available_figures: set[int] = set()
    for fig_dir_name in FIGURE_DIRS:
        fig_dir = project_dir / fig_dir_name
        if fig_dir.exists():
            for f in fig_dir.iterdir():
                if f.is_file():
                    # Try to extract figure number from filename (e.g., f1_xxx.png)
                    m = re.match(r"f(\d+)[_.]", f.name, re.IGNORECASE)
                    if m:
                        available_figures.add(int(m.group(1)))

    missing_numbered = []
    for fig_num, line_num in figure_refs:
        if available_figures and fig_num not in available_figures:
            missing_numbered.append((fig_num, line_num))
            findings.append(Finding(
                "figure_existence", "WARNING",
                f"Figure {fig_num} referenced but no matching file found (f{fig_num}_*)",
                line=line_num,
            ))

    total_refs = len(image_paths) + len(figure_refs)
    total_missing = len(missing_images) + len(missing_numbered)

    if total_refs > 0 and total_missing == 0:
        findings.append(Finding(
            "figure_existence", "PASS",
            f"All {total_refs} figure/image references resolved",
        ))
    elif total_refs == 0:
        findings.append(Finding(
            "figure_existence", "WARNING",
            "No figure or image references found in FINDINGS.md",
        ))

    return findings


