#!/usr/bin/env python3
"""check_findings.py — Validate FINDINGS.md claims against output artifacts.

Auto-generated by gen_findings_audit.py from project.yaml.
Re-run generator after changing project.yaml audit configuration.

Project: Experiment

This script:
  1. Reads FINDINGS.md and extracts all claim tags, evidence paths, numeric values,
     hypothesis references, and figure references.
  2. Validates that every [DEMONSTRATED] claim has an evidence artifact that exists.
  3. Cross-references numeric values against output JSON/CSV data.
  4. Verifies hypothesis references (H-X) exist in HYPOTHESIS_REGISTRY.md.
  5. Checks that referenced figures exist in figures/ or blog/images/.
  6. Scans for prohibited language in [DEMONSTRATED] claims.
  7. Reports claim tag coverage percentage.

Exit codes:
  0 — All checks passed
  1 — One or more CRITICAL checks failed
"""
