def check_evidence_artifacts(
    claims: list[dict],
    evidence_paths: list[tuple[str, int]],
    project_dir: Path,
    outputs_dir: Path,
) -> list[Finding]:
    """Verify that every [DEMONSTRATED] claim has an evidence artifact that exists."""
    findings: list[Finding] = []

    # Collect all demonstrated claim line numbers
    demonstrated_lines = set()
    for c in claims:
        if c["tag"] == "DEMONSTRATED":
            demonstrated_lines.add(c["line_num"])

    # Check all evidence paths exist
    missing_artifacts = []
    found_artifacts = []
    for path_str, line_num in evidence_paths:
        # Try multiple resolution strategies
        candidates = [
            project_dir / path_str,
            outputs_dir / path_str,
            project_dir / "outputs" / path_str,
        ]
        found = False
        for candidate in candidates:
            if candidate.exists():
                found = True
                found_artifacts.append((path_str, line_num))
                break
        if not found:
            missing_artifacts.append((path_str, line_num))
            findings.append(Finding(
                "evidence_artifact", "CRITICAL",
                f"Evidence artifact not found: {path_str}",
                line=line_num,
            ))

    # Check that demonstrated claims near evidence paths have valid artifacts
    # (within 5 lines of the claim)
    for c in claims:
        if c["tag"] != "DEMONSTRATED":
            continue
        nearby_evidence = [
            p for p, ln in evidence_paths
            if abs(ln - c["line_num"]) <= 5
        ]
        if not nearby_evidence:
            findings.append(Finding(
                "evidence_artifact", "WARNING",
                f"[DEMONSTRATED] claim has no nearby evidence path reference",
                line=c["line_num"],
            ))

    if not missing_artifacts and demonstrated_lines:
        findings.append(Finding(
            "evidence_artifact", "PASS",
            f"All {len(found_artifacts)} evidence artifact paths resolve to existing files",
        ))
    elif not evidence_paths:
        findings.append(Finding(
            "evidence_artifact", "WARNING",
            "No evidence artifact paths found in FINDINGS.md",
        ))

    return findings


