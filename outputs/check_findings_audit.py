def print_summary(findings: list[Finding]) -> None:
    """Print human-readable summary."""
    critical = [f for f in findings if f.severity == "CRITICAL"]
    warnings = [f for f in findings if f.severity == "WARNING"]
    passes = [f for f in findings if f.severity == "PASS"]

    print()
    print("=" * 70)
    print("FINDINGS AUDIT RESULTS")
    print("=" * 70)
    print(f"  CRITICAL: {len(critical)}")
    print(f"  WARNING:  {len(warnings)}")
    print(f"  PASS:     {len(passes)}")
    print("-" * 70)

    if critical:
        print()
        print("CRITICAL FAILURES:")
        for f in critical:
            loc = f" (line {f.line})" if f.line else ""
            print(f"  [CRITICAL] [{f.check}]{loc}: {f.message}")
        print()

    if warnings:
        print("WARNINGS:")
        for f in warnings:
            loc = f" (line {f.line})" if f.line else ""
            print(f"  [WARNING] [{f.check}]{loc}: {f.message}")
        print()

    # Per-check verdict table
    print("PER-CHECK VERDICTS:")
    checks_seen = []
    for f in findings:
        if f.check not in checks_seen:
            checks_seen.append(f.check)

    for check_name in checks_seen:
        check_findings = [f for f in findings if f.check == check_name]
        has_critical = any(f.severity == "CRITICAL" for f in check_findings)
        has_warning = any(f.severity == "WARNING" for f in check_findings)
        if has_critical:
            verdict = "FAIL (CRITICAL)"
        elif has_warning:
            verdict = "WARN"
        else:
            verdict = "PASS"
        print(f"  {check_name:<25} {verdict}")

    print()
    if critical:
        print(f"  RESULT: FAIL ({len(critical)} critical issues)")
    else:
        print("  RESULT: PASS")
    print("=" * 70)


def write_findings_json(findings: list[Finding], output_path: Path) -> None:
    """Write findings to JSON."""
    report = {
        "summary": {
            "total": len(findings),
            "critical": sum(1 for f in findings if f.severity == "CRITICAL"),
            "warning": sum(1 for f in findings if f.severity == "WARNING"),
            "pass": sum(1 for f in findings if f.severity == "PASS"),
        },
        "findings": [
            {
                "check": f.check,
                "severity": f.severity,
                "message": f.message,
                "line": f.line,
            }
            for f in findings
        ],
    }
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(json.dumps(report, indent=2) + "\n")
    print(f"  JSON findings written: {output_path}")


