def check_claim_coverage(
    claims: list[dict],
    numbers: list[dict],
    text: str,
) -> list[Finding]:
    """Check that claim tag coverage is 100% of quantitative findings."""
    findings: list[Finding] = []

    # Count lines that contain quantitative claims (numbers in non-header lines)
    quantitative_lines = set()
    for entry in numbers:
        quantitative_lines.add(entry["line_num"])

    # Count lines that have claim tags
    tagged_lines = set()
    for c in claims:
        tagged_lines.add(c["line_num"])

    total_claims = len(claims)

    if total_claims == 0:
        findings.append(Finding(
            "claim_coverage", "WARNING",
            "No claim tags found in FINDINGS.md — expected 100% coverage",
        ))
        return findings

    # Count claims by tag
    tag_counts: dict[str, int] = {}
    for c in claims:
        tag_counts[c["tag"]] = tag_counts.get(c["tag"], 0) + 1

    # Calculate coverage: tagged quantitative lines / total quantitative lines
    tagged_quant = quantitative_lines & tagged_lines
    if quantitative_lines:
        coverage = len(tagged_quant) / len(quantitative_lines) * 100
    else:
        coverage = 100.0  # no quantitative lines = vacuous pass

    sev = "PASS" if coverage >= 90 else ("WARNING" if coverage >= 50 else "CRITICAL")
    tag_summary = ", ".join(f"{t}={n}" for t, n in sorted(tag_counts.items()))
    findings.append(Finding(
        "claim_coverage", sev,
        f"Claim tag coverage: {coverage:.0f}% ({len(tagged_quant)}/{len(quantitative_lines)} "
        f"quantitative lines tagged). Tags: {tag_summary}. Total claims: {total_claims}.",
    ))

    return findings


